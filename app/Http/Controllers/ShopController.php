     */
    public function index($id)
    {
        $userFavShop = User::find($id)->shops()->get();
        $notFav = collect();
        foreach ($userFavShop as $shop) {
          $notFav->push($shop->id);
        }
        $shops = Shop::whereNotIn("id", $notFav->all())->get();
        return response()->json(['success' => true, 'data' => $shops], 201);
    }

